const languages = ['en', 'hi', 'ta', 'te', 'bn', 'mr', 'gu', 'kn', 'ml', 'ms', 'ne'];

// Update each language file
languages.forEach(lang => {
  const translationPath = path.join(__dirname, `../public/locales/${lang}/translation.json`);
  
  try {
    // Read existing translations
    const existing = JSON.parse(fs.readFileSync(translationPath, 'utf8'));
    
    // Get additional translations for this language
    const additionalForLang = additionalTranslations[lang] || {};
    
    // Merge with existing translations
    // NOTE: Existing translations take precedence to avoid overwriting manual edits
    // If you need to force update an existing key, delete it from the translation file first
    const updated = {
      ...additionalForLang,
      ...existing,
    };
    
    // Sort keys alphabetically for consistency
    const sorted = Object.keys(updated)
      .sort()
      .reduce((acc, key) => {
        acc[key] = updated[key];
        return acc;
      }, {});
    
    // Write back to file with pretty formatting
    fs.writeFileSync(translationPath, JSON.stringify(sorted, null, 2) + '\n', 'utf8');
    
    console.log(`✓ Updated ${lang}/translation.json with UI translations`);
  } catch (error) {
    console.error(`✗ Failed to update ${lang}/translation.json:`, error.message);
  }
});

